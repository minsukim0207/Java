package com.kh.operator;
 
public class Arithmetic {

	public static void main(String[] args) {
		// 1. 산술 연산자 (Arithmetic)
		// 더하기(+), 빼기(-), 곱하기(*), 나누기 몫(/), 나누기 나머지(%)
		
		int num1 = 5;
		int num2 = 2;
		
		System.out.println(num1 + num2);
		System.out.println(num1 - num2);
		System.out.println(num1 * num2);
		System.out.println(num1 / num2);
		System.out.println(num1 % num2);
	}

}
